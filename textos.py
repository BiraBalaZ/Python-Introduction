# Manipulando textos 
texto = 'Hello World' # óbvio...

print(f'{texto.upper()}')  # Deixa o texto em MAIÚSCULO
print(f'{texto.lower()}')  # Deixa o texto em minúsculo

# Imprimindo cada letra separadamente usando um loop for
for letra in texto:
    print(letra)

for letra in texto:
    print(letra, end=' ')

